const soma = function(a, b){
  return a + b;
}

function subtrair (a, b){
  let resultado = a - b
  return resultado;
}

const mutiplicar = (a, b) => {
  return a * b;
}

const dividir = (a, b) => a / b;

console.log(soma(10, 5));
console.log(subtrair(10, 5));
console.log(mutiplicar(10, 5));
console.log(dividir(10, 5));